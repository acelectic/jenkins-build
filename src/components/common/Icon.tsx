export type IconNameType = typeof IconName

export type IconNameKeys = keyof IconNameType

export interface IconProps {
  iconName: IconNameKeys
  height?: number
  width?: number
  fill?: string
  stroke?: string
  style?: CSSProperties
  onClick?: (event?: MouseEvent<HTMLButtonElement>) => void
  className?: string
}

const Icon = (props: IconProps) => {
  const {
    iconName,
    height = 30,
    width = 30,
    fill = "none",
    stroke,
    style,
    onClick,
    className,
    ...restProps
  } = props

  const IconNameCard = useMemo(() => {
    return IconName[iconName]
  }, [iconName])

  return onClick ? (
    <IconButton onClick={onClick}>
      <IconNameCard
        fill={fill}
        stroke={stroke}
        height={`${height}px`}
        width={`${width}px`}
        style={{ ...style }}
        className={`${className}`}
        {...restProps}
      />
    </IconButton>
  ) : (
    <IconNameCard
      fill={fill}
      stroke={stroke}
      height={`${height}px`}
      width={`${width}px`}
      style={{ ...style }}
      className={`${className}`}
      {...restProps}
    />
  )
}

export default Icon
